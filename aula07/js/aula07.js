// função somar

function somar (num1, num2){
    return num1 + num2;

}
console.log(somar(5,4));

// função subtrair

function subtrair (num1, num2){
    return num1 - num2;

}
console.log(subtrair(12,4));

// função dividir

function dividir (num1, num2){
    return num1 / num2;

}
console.log(dividir(15,5));

// função multiplicar

function multiplicar (num1, num2){
    return num1 * num2;

}
console.log(multiplicar(5,5));
